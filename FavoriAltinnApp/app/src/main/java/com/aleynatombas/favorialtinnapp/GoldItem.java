package com.aleynatombas.favorialtinnapp;

public class GoldItem {
    private String name;
    private String price;

    public GoldItem(String name, String price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public String getPrice() {
        return price;
    }
}
